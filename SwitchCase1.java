public class SwitchCase1 {
    public static void main(String[] args) {
        int num = 40;
        switch(num){
            case 10:{
                System.out.println("Number is 10");
            break;
            }
            case 20:{
                System.out.println("Number is 20");
            break;
            }
            case 30:{
                System.out.println("Number is 30");
            break;
            }
            case 40:{
                System.out.println("Number is 40");
            break;
            }
            case 50:{
                System.out.println("Number is 50");
            break;
            }
            case 60:{
                System.out.println("Number is 60");
            break;
            }
            case 70:{
                System.out.println("Number is 70");
            break;
            }
            case 80:{
                System.out.println("Number is 80");
            break;
            }
            case 90:{
                System.out.println("Number is 90");
            break;
            }
            default:
            {
                System.out.println("Invalid number..!!");
            }

            
        }
    }
}
